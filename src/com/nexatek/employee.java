    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try {
        String searchTerm = search_id.getText().trim();

        if (searchTerm.isEmpty()) {
            // If the search term is empty, display a message
            JOptionPane.showMessageDialog(null, "Please enter a search term", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Construct the SQL query for searching
        String sql = "SELECT * FROM employeestbl WHERE empname LIKE ? OR emptel LIKE ? OR empaddress LIKE ? OR role LIKE ?";
        pst = conn.prepareStatement(sql);
        pst.setString(1, "%" + searchTerm + "%");
        pst.setString(2, "%" + searchTerm + "%");
        pst.setString(3, "%" + searchTerm + "%");
        pst.setString(4, "%" + searchTerm + "%");

        // Execute the search query
        rst = pst.executeQuery();

        // Update the table with the search results
        employeestbl.setModel(DbUtils.resultSetToTableModel(rst));
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_searchActionPerformed

