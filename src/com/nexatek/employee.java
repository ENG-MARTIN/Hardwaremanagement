    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
    try {
        Date j_Date = JoiningDate.getDate();
        int selectedRow = employeestbl.getSelectedRow();

        if (selectedRow == -1) {
            // If no row is selected, display a message
            JOptionPane.showMessageDialog(null, "Please select a row to update", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String empId = employeestbl.getValueAt(selectedRow, 0).toString();
        
        // Update the database with the new values
        String sql = "UPDATE employeestbl SET empname=?, emptel=?, empaddress=?, role=?, sex=?, age=?, joiningdate=? WHERE id=?";
        pst = conn.prepareStatement(sql);
        pst.setString(1, nametxt.getText());  
        pst.setString(2, teltxt.getText());     
        pst.setString(3, addresstxt.getText());  
        pst.setString(4, role.getText());     
        pst.setString(5, sex.getSelectedItem().toString());     
        pst.setInt(6, Integer.parseInt(age.getText()));         
        pst.setDate(7, new java.sql.Date(j_Date.getTime()));
        pst.setInt(8, Integer.parseInt(empId)); // Set the ID using the correct parameter index
        
        int affectedRows = pst.executeUpdate();
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(null, "Record updated successfully");
            Update_table();
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update record", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, e);   
}

    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
    try {
        int selectedRow = employeestbl.getSelectedRow();

        if (selectedRow == -1) {
            // If no row is selected, display a message
            JOptionPane.showMessageDialog(null, "Please select a row to delete", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String empNameValue = employeestbl.getValueAt(selectedRow, 1).toString();

        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected record?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        if (option == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM employeestbl WHERE empname=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, empNameValue);
            int affectedRows = pst.executeUpdate();

            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(null, "Record deleted successfully");
                Update_table(); // Update the table with the new data
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete record", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, e);
    }

    }//GEN-LAST:event_deleteActionPerformed


